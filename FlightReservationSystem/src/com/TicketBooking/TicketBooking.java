package com.TicketBooking;

public class TicketBooking {
	
	String Flight_Num, Flight_Date, Seat_Number, Customer_Name, Customer_Ph_No;

	public String getFlight_Num() {
		return Flight_Num;
	}

	public void setFlight_Num(String flight_Num) {
		Flight_Num = flight_Num;
	}

	public String getFlight_Date() {
		return Flight_Date;
	}

	public void setFlight_Date(String flight_Date) {
		Flight_Date = flight_Date;
	}

	public String getSeat_Number() {
		return Seat_Number;
	}

	public void setSeat_Number(String seat_Number) {
		Seat_Number = seat_Number;
	}

	public String getCustomer_Name() {
		return Customer_Name;
	}

	public void setCustomer_Name(String customer_Name) {
		Customer_Name = customer_Name;
	}

	public String getCustomer_Ph_No() {
		return Customer_Ph_No;
	}

	public void setCustomer_Ph_No(String customer_Ph_No) {
		Customer_Ph_No = customer_Ph_No;
	}
}
