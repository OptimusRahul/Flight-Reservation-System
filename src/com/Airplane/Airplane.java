package com.Airplane;

public class Airplane {
	
	String Airplane_Id, Airplane_Type;
	int Airplane_total_seats;
		
	public String getAirplane_Id() {
		return Airplane_Id;
	}
	public void setAirplane_Id(String airplane_Id) {
		Airplane_Id = airplane_Id;
	}
	public String getAirplane_Type() {
		return Airplane_Type;
	}
	public void setAirplane_Type(String airplane_Type) {
		Airplane_Type = airplane_Type;
	}
	public int getAirplane_total_seats() {
		return Airplane_total_seats;
	}
	public void setAirplane_total_seats(int airplane_total_seats) {
		Airplane_total_seats = airplane_total_seats;
	}

}
